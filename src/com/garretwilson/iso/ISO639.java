package com.garretwilson.iso;

/**ISO 639 Language Codes
These codes are the shortest available of ISO 639-1 and ISO 639-2 codes, as outlined in
<a href="http://www.ietf.org/rfc/rfc4646.txt">RFC 4646: "Tags for the Identifying Languages"</a>.
@author Garret Wilson
@see <a href="http://www.loc.gov/standards/iso639-2/">ISO 639-2</a>
@see <a href="http://www.ietf.org/rfc/rfc4646.txt">RFC 4646</a>
*/
public class ISO639
{
	/**Arabic*/
	public final static String ARABIC_CODE="ar"; 
	/**Farsi*/
	public final static String FARSI_CODE="fa"; 
	/**Hebrew*/
	public final static String HEBREW_CODE="he"; 
	/**The obsolete code for Hebrew, used by Java.*/
	public final static String HEBREW_OBSOLETE_CODE="iw"; 
	/**Urdu*/
	public final static String URDU_CODE="ur"; 
}